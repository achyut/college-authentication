    private void displayFacultyInfo() {
        this.name.setText(Faculty.fct.name);
        this.id.setText(Faculty.fct.id);
        this.phoneno.setText(Faculty.fct.phone);
        this.address.setText(Faculty.fct.address);
        this.dept.setText(Faculty.fct.department);
        this.post.setText(Faculty.fct.profile);
        this.validUpto.setText(((Integer) Faculty.fct.validUpto).toString());
        this.registeredat.setText(Faculty.fct.addtimestamp);
        if (Faculty.fct.image != null) {
            ImageIcon thumbnailIcon = new ImageIcon(Utilities.util.rescaleImage(Faculty.fct.image, this.photoPanel.getWidth(), this.photoPanel.getHeight()));
            this.pic.setIcon(thumbnailIcon);
        }        
    }

    public FacultyDetails(String rollNo, String name, String type) {
        
        if (Faculty.fct.setFacultyInfo(rollNo, name) == true) {
            initComponents();
            setLocationRelativeTo(null);
            displayFacultyInfo();
            ActionType = type;
            if (type.equalsIgnoreCase("submit")) {
                jPanel4.setBorder(javax.swing.BorderFactory.createTitledBorder(new javax.swing.border.LineBorder(new java.awt.Color(0, 0, 0), 2, true), "Are the information correct?", javax.swing.border.TitledBorder.DEFAULT_JUSTIFICATION, javax.swing.border.TitledBorder.DEFAULT_POSITION, new java.awt.Font("Arial", 1, 14), new java.awt.Color(255, 0, 0))); // NOI18N
            }
            
            if (type.equalsIgnoreCase("edit/delete")) {
                back.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icon/32/delete.png")));
                save.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icon/32/edit.png")));
                this.save.setText("Edit");
                this.back.setText("Delete");
            }
            
            if (type.equalsIgnoreCase("view")) {
                save.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icon/32/ok.png")));
                this.save.setText("Ok");
                this.back.hide();
            }
            if (type.equalsIgnoreCase("print")) {
                save.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icon/32/print.png")));
                this.save.setText("Print");
                this.back.hide();
            }
            if (type.equalsIgnoreCase("resetpass")) {
                save.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icon/32/change_pass.png")));
                this.save.setText("Reset");
                this.back.hide();
            }
            else {
                this.dispose();
            }
        }
    }

