    void submit() {
        if (this.ActionType.equalsIgnoreCase("")) {
            if (Faculty.fct.ifFacultyExists(Faculty.fct.id, "")) {
                DialogBox.dialog.successDialog("Faculty with " + Faculty.fct.id + " already exists");
                FacultyDetailsForm fctreg = new FacultyDetailsForm("back");
                fctreg.getPrevious();
                fctreg.setVisible(true);
            } else if (Faculty.fct.addFaculty() == true) {
                DialogBox.dialog.successDialog("faculty Record Sucessfully added");
                int choice = DialogBox.dialog.confirmDialog("Do you want to add another faculty??");
                if (choice == 0) {
                    FacultyDetailsForm fctreg = new FacultyDetailsForm();
                    fctreg.setVisible(true);
                } else {
                    this.dispose();
                }
            }
        } else if (this.ActionType.equalsIgnoreCase("edit")) {
            if (Faculty.fct.editFaculty() == true) {
                DialogBox.dialog.successDialog("Faculty record SucessFully Edited!!!");// TODO add your handling code here:
                EnterRoll er = new EnterRoll("edit/delete", "faculty");
                er.setVisible(true);
            }
        }
        else if (this.ActionType.equalsIgnoreCase("resetpass")) {
            PasswordResetBox pr = new PasswordResetBox(Faculty.fct.id,"faculty");
            pr.setVisible(true);
        }
        
        if (save.getText().equalsIgnoreCase("edit")) {
            FacultyDetailsForm fctreg = new FacultyDetailsForm("edit");
            fctreg.getPrevious();
            fctreg.setVisible(true);
        }
        if (save.getText().equalsIgnoreCase("print")) {
         Report rep = new Report();
         rep.printFacultyInfo(Faculty.fct.id);
        }
        

        /*
         * if(save.getText().equalsIgnoreCase("ok")) { EnterRoll er=new
         * EnterRoll("view","faculty"); er.setVisible(true); }
         */        
        this.dispose();        
    }

    void back() {
        if (back.getText().equalsIgnoreCase("delete")) {
            
            int i = DialogBox.dialog.confirmDialog("Are you sure you want to delete info of" + Faculty.fct.id + "?");
            if (i == 0) {
                if (Faculty.fct.deleteFaculty() == true) {
                    DialogBox.dialog.successDialog("faculty record SucessFully Deleted!!!");// TODO add your handling code here:
                } else {
                    DialogBox.dialog.errorDialog("Error in Deleting faculty's information");
                }
            }
            EnterRoll er = new EnterRoll("edit/delete", "faculty");
            er.setVisible(true);
        } else if (back.getText().equalsIgnoreCase("print")) {
            Report report = new Report();
            report.printFacultyInfo(Faculty.fct.id);
        } else {
            if (ActionType.equalsIgnoreCase("")) {
                FacultyDetailsForm fctreg = new FacultyDetailsForm("back");
                fctreg.getPrevious();
                fctreg.setVisible(true);
            } else {
                FacultyDetailsForm fctreg = new FacultyDetailsForm("edit");
                fctreg.getPrevious();
                fctreg.setVisible(true);
            }
        }
        this.dispose();
        
    }
    
