    private void captureActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_captureActionPerformed

        if (capture.isSelected()) {
            Webcam.cam.captureImage();
            BufferedImage bi = Webcam.cam.getBufferedImage();
            Faculty.fct.image = bi;
            Faculty.fct.fingerprint = bi;
            Webcam.cam.stopCam();
        } else if (!capture.isSelected()) {
            Webcam.cam.startCam();
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_captureActionPerformed

