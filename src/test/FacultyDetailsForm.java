    void submit()
    {   if(putFacultyInfo()){
          if(action.equalsIgnoreCase("edit")){
              FacultyDetails fctDetail = new FacultyDetails("edit");
              fctDetail.setVisible(true);
                }
          if(action.equalsIgnoreCase("delete")){
              FacultyDetails fctDetail = new FacultyDetails("delete");
              fctDetail.setVisible(true);
              }
          else if(action.equalsIgnoreCase("") || action.equalsIgnoreCase("back")){
            FacultyDetails fctDetail = new FacultyDetails(1);
            fctDetail.setVisible(true);
                }
             this.dispose();
        }
    }
    
        
    boolean putFacultyInfo(){
    if(Validation.val.validateFacultyInfo(this.facultyId.getText(),this.facultyName.getText(),this.facultyAddress.getText(),this.facultyPhone.getText(),this.facultyDept.getText(),this.facultyPost.getText(),this.facultyValidUpto.getText()) == true){            
        Faculty.fct.name = this.facultyName.getText();
        Faculty.fct.id = this.facultyId.getText();
        Faculty.fct.address = this.facultyAddress.getText();
        Faculty.fct.phone = this.facultyPhone.getText();
        Faculty.fct.profile = this.facultyPost.getText();
        Faculty.fct.department =this.facultyDept.getText();
        Faculty.fct.validUpto = Integer.parseInt(this.facultyValidUpto.getText());
        
        if(!action.equalsIgnoreCase("edit")){
        Faculty.fct.addtimestamp=TimeDate.time.timestampToFullDate(AbstractPerson.ab.currentTimeStamp());
        Faculty.fct.addedby=Session.s1.sessionUser;
        }
        Faculty.fct.password = this.facultyId.getText();
        return true;
        }
    else 
        return false;
    }
