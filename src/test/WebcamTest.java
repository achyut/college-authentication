    void keyAction(java.awt.event.KeyEvent evt, String Button) {
        int key = evt.getKeyCode();
        if (key == KeyEvent.VK_ENTER) {
            if (Button.equalsIgnoreCase("cancel")) {
                this.dispose();
            } else if (Button.equalsIgnoreCase("submit")) {
                capture();
            }
        }
        if (key == KeyEvent.VK_ESCAPE) {
            Webcam.cam.closeCam();
            this.dispose();
        }
    }
