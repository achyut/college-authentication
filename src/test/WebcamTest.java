    void capture(){
    if(captureButton.isSelected()){
           Webcam.cam.captureImage();
           BufferedImage bi = Webcam.cam.getBufferedImage();
          if(window.equalsIgnoreCase("student")){
           Student.std.image = bi;
          }
          else if(window.equalsIgnoreCase("guest")){
              Guest.gst.image = bi;
          }
          else if(window.equalsIgnoreCase("faculty")){
              Faculty.fct.image = bi;
          }
           Webcam.cam.stopCam();
        }
        else if(!captureButton.isSelected())
        {
           Webcam.cam.startCam();
        }
    }
