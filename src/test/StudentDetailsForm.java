    private void displayStudentInfo() {
        this.name.setText(Student.std.name);
        this.roll.setText(Student.std.id);
        this.year.setText(((Integer) Student.std.year).toString());
        this.address.setText(Student.std.address);
        this.department.setText(Student.std.branch);
        this.phone.setText(Student.std.phone);
        this.mac1.setText(Student.std.macAdd1);
        this.mac2.setText(Student.std.macAdd2);
        this.validUpto.setText(((Integer) Student.std.validUpto).toString());
        this.registeredat.setText(Student.std.addtimestamp);
        if (Student.std.image != null) {
            ImageIcon thumbnailIcon = new ImageIcon(Utilities.util.rescaleImage(Student.std.image, this.photoPanel.getWidth(), this.photoPanel.getHeight()));
            this.photo.setIcon(thumbnailIcon);
        }
    }

