    void submit() {
        if (this.ActionType.equalsIgnoreCase("")) {

            if (Student.std.ifStudentExists(Student.std.id, "")) {
                DialogBox.dialog.successDialog("Student with " + Student.std.id + " already exists");
                StudentRegistration streg = new StudentRegistration("");
                streg.getPrevious();
                streg.setVisible(true);
            } else if (Student.std.addStudent() == true) {
                DialogBox.dialog.successDialog("Student Record Sucessfully added");
                StudentRegistration streg = new StudentRegistration();
                streg.resetItems();
                streg.setVisible(true);
            }
                    
        } else if (this.ActionType.equalsIgnoreCase("edit")) {
            if (Student.std.editStudent() == true) {
                DialogBox.dialog.successDialog("Student SucessFully Edited!!!");// TODO add your handling code here:
                EnterRoll er = new EnterRoll("edit/delete", "student");
                er.setVisible(true);
            }
        }
           else if (this.ActionType.equalsIgnoreCase("resetpass")) {
            PasswordResetBox pr = new PasswordResetBox(Student.std.id,"student");
            pr.setVisible(true);
        }
        else if (save.getText().equalsIgnoreCase("edit")) {
            StudentRegistration std = new StudentRegistration("edit");
            std.getPrevious();
            std.setVisible(true);
        } 
        else if (save.getText().equalsIgnoreCase("print")) {
            Report report = new Report();
            report.printStudentInfo(Student.std.id);
        }
        
        /*
         * else if(save.getText().equalsIgnoreCase("ok")) {
         *
         * EnterRoll er=new EnterRoll("view","student"); er.setVisible(true); }
         */
        this.dispose();
    }

    void back() {
        if (back.getText().equalsIgnoreCase("delete")) {

            int i = DialogBox.dialog.confirmDialog("Are you sure you want to delete info of" + Student.std.id + "?");
            if (i == 0) {
                if (Student.std.deleteStudent() == true) {
                    DialogBox.dialog.successDialog("Student SucessFully Deleted!!!");// TODO add your handling code here:
                } else {
                    DialogBox.dialog.errorDialog("Error in Deleting Student information");
                }
            }
            EnterRoll er = new EnterRoll("edit/delete", "student");
            er.setVisible(true);


        } else {
            if (ActionType.equalsIgnoreCase("")) {
                StudentRegistration streg = new StudentRegistration("");
                streg.getPrevious();
                streg.setVisible(true);
            } else {
                StudentRegistration streg = new StudentRegistration("edit");
                streg.getPrevious();
                streg.setVisible(true);// TODO add your handling code here:
            }
        }
        this.dispose();

    }


